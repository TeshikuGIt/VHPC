DROP TABLE IF EXISTS reading_list;

CREATE TABLE IF NOT EXISTS reading_list (
  id INT AUTO_INCREMENT PRIMARY KEY,
  `Barcode` VARCHAR(255),
  `Test time` VARCHAR(50),
  `E(lx)` DECIMAL(12,2),
  `CCT(K)` INT,
  `Ra` DECIMAL(12,2),
  `R1` DECIMAL(12,2),
  `R2` DECIMAL(12,2),
  `R3` DECIMAL(12,2),
  `R4` DECIMAL(12,2),
  `R5` DECIMAL(12,2),
  `R6` DECIMAL(12,2),
  `R7` DECIMAL(12,2),
  `R8` DECIMAL(12,2),
  `R9` DECIMAL(12,2),
  `R10` DECIMAL(12,2),
  `R11` DECIMAL(12,2),
  `R12` DECIMAL(12,2),
  `R13` DECIMAL(12,2),
  `R14` DECIMAL(12,2),
  `R15` DECIMAL(12,2),
  `Ee(mW/cm2)` DECIMAL(12,2),
  `failed/passed` VARCHAR(20)
);